                                <h3>Medicare Supplement</h3>
                                <p>Commonly known as Medigap, Medicare Supplement Insurance plans help pay some of your out-of-pocket costs under Original Medicare. Private insurance companies provide Medigap plans, with Medicare establishing guidelines for what each plan covers.</p>
                                <h6>What Is Medigap?</h6> 
                                <p>A Medicare Supplement plan pays some of the costs not covered by Original Medicare (Parts A and B). Which costs the plan covers depends on which Medigap plan you choose.</p> 
                                <p>There are 10 Medigap plans available in most states, although Plans C and F are no longer available to people who became eligible for Medicare after December 31, 2019.</p> 
                                <p>In addition to federal oversight, each state has its own Medigap regulations. This is why you don’t find all 10 Medigap plans in every state. Some states also offer high-deductible versions of two Supplement plans (G and F). And three states – Massachusetts, Minnesota, and Wisconsin – standardize their Medigap offerings in a completely different way.</p>
                                <p>Medicare.gov offers full details regarding supplement insurance at each of the following links:</p>
                                <ul class="link-block">
                                    <li><a href="javascript:void(0);">Massachusetts</a></li>
                                    <li><a href="javascript:void(0);">Minnesota</a></li>
                                    <li><a href="javascript:void(0);">Wisconsin</a></li>
                                </ul> 
                                <p>Finally, Medigap plans are standardized, meaning every Plan A (or Plan B, C, etc.) offers the exact same coverage, no matter which insurance company you choose. However, supplemental insurance companies are allowed to set their own rates.</p> 
                                <h6>Comparing Medicare Supplemental Insurance Companies Because</h6> 
                                <p>Medigap policies are standardized, shopping for supplemental insurance typically comes down to price and the insurance company’s reputation.</p> 
                                <p>Insurers also have some leeway as to which supplement plans they offer. However, if the insurance company chooses to sell Medigap policies, they must offer Medigap Plan A. Our Find a Plan tool lets you compare the Medicare Supplement plans available in your area. Just enter your location, estimated coverage start date, and hit Continue.</p> 
                                <h6>When Can You Get a Medicare Supplement Plan?</h6> 
                                <p>Anyone who is over 65 and enrolled in Original Medicare may apply for a Supplement plan at any time. And some states allow anyone enrolled in Medicare to apply for Medigap – even if they are not age 65 or older. However, just because you can apply at any time does not mean the insurance company has to sell you a plan. Unless, of course, you qualify for guaranteed issue rights. At any other time, the insurance company bases its decision on a process known as medical underwriting.</p> 
                                <p>Please note that you cannot have both Medicare Advantage (MA) and a supplement plan.</p> 
                                <h6>When Are You Guaranteed a Medigap Plan?</h6> 
                                <p>As soon as you are both enrolled in Original Medicare AND aged 65 or older, you enter your Medigap Open Enrollment Period (OEP). Your OEP lasts for 6 months and during this time, you cannot be denied a Medigap policy or charged more for your plan – no matter what preexisting conditions you have.</p> 
                                <p>The same holds true when you qualify for guaranteed issue rights. The most common scenario that gives you this right is joining your first Medicare Advantage plan and then returning to Original Medicare within 12 months. Your guaranteed issue window lasts for 4 months:</p> 
                                <ul>
                                    <li>The window opens 2 months before your MA plan ends</li>
                                    <li>It closes 2 months after you make the switch to Original Medicare</li>
                                </ul>
                                <p>You may also qualify for guaranteed issue rights if you lose your MA or Medigap plan through no fault of your own.</p> 
                                <p>Unless you qualify for guaranteed issue rights, applying for a Medigap plan outside of your OEP means you have to go through medical underwriting.</p> 
                                <h6>What Is Medical Underwriting?</h6> 
                                <p>Medical underwriting is how the insurance company determines how likely it is you’ll make a claim. Your Medigap application includes a number of health-related questions. This includes:</p> 
                                <ul>
                                    <li>Age</li>
                                    <li>Weight</li>
                                    <li>Blood pressure</li>
                                    <li>Any chronic conditions you have</li>
                                </ul>
                                <p>You will also see questions about your personal and family medical history. The insurance company uses your answers to these questions to determine whether to sell you a Medigap policy, as well as how much to charge you.</p> 
                                <p>If you are within your Medigap Open Enrollment Period, your application cannot be denied based on underwriting. You also cannot be charged a higher monthly premium. The same is true if you apply for Medigap when you have guaranteed issue rights. At any other time, though, Medicare Supplement insurance companies may either reject your application or charge a higher premium.</p> 
                                <h6>How Much Does Medigap Cost?</h6> 
                                <p>There is no set cost for Medicare Supplement Insurance. Instead, insurance companies set their rates in one of three ways.</p> 
                                <div class="other-details">
                                    <p><strong>Attained-age rated:</strong> Your current age dictates your premium, which means your premium increases along with your age.</p> 
                                    <p><strong>Community rated:</strong> Everyone pays the same premium, regardless of age.</p> 
                                    <p><strong>Issue-age rated:</strong> Your age at the time you buy the policy determines your premium, which means you get a lower premium the earlier you buy your policy.</p> 
                                </div>
                                <p>Where you live may also affect your costs. Ask the insurance company what factors they consider when setting premiums.</p> 
                                <h6>Does Medicare Supplement Insurance Cover Pre-Existing Conditions?</h6> 
                                <p>If you are within your Medigap OEP or have guaranteed issue rights, you cannot be denied a Medigap plan. However, the insurance company may enforce a 6-month waiting period for coverage of any pre-existing conditions. Compare quotes and details carefully when choosing a supplemental insurance company.</p> 
                                <h6>What Does Medigap Cover?</h6> 
                                <p>Medigap coverage varies based on which plan you choose.</p> 
                                <p>The one thing all Medicare Supplement plans cover is your Medicare Part A coinsurance. They also give you an extra 365 “lifetime reserve” days for inpatient hospital care. (Without a Medigap plan, you get 60 lifetime reserve days under Part A.)</p>
                                <p>All Medigap plans also cover at least part of the following:</p>
                                <ul class="no-spacing">
                                    <li>Medicare Part B coinsurance or copayment</li>
                                    <li>The first three pints of blood needed for a transfusion</li>
                                    <li>Medicare Part A hospice care coinsurance or copayment</li>
                                </ul>
                                <p>Plan K and Plan L cover the above at 50 and 75 percent, respectively. All other Medigap plans cover these items at 100 percent.</p> <p>Other items covered by Medicare Supplement Insurance include:</p> 
                                <div class="other-details">
                                    <p><strong>Skilled nursing facility care coinsurance:</strong> Plans C, D, F, G, M, and N. Plan K covers 50 percent and Plan L covers 75 percent.</p> 
                                    <p><strong>Part A deductible:</strong> Plans B, C, D, F, G, M, and N. Plan K covers 50 percent and Plan L covers 75 percent.</p> 
                                    <p><strong>Part B deductible:</strong> Plan C and Plan F.</p> 
                                    <p><strong>Part B excess charge:</strong> Plan F and Plan G.</p> 
                                    <p><strong>Foreign travel exchange:</strong> Plans C, D, F, G, M, and N cover 80 percent.</p> 
                                    <p><strong>Out-of-pocket limit:</strong> Plan K has an annual out-of-pocket limit of $5,880. Plan L’s out-of-pocket maximum is $2,940. No other plans have an annual limit.</p> 
                                </div>
                                <p>Please note that if you became eligible for Medicare in 2020, you may not enroll in Medigap Plan F or Plan C. Medigap Plan G offers all of the same coverage as Plan F except for the Part B deductible. However, the lower premiums found with Plan G mean you usually have lower out-of-pocket costs than with Plan F.</p> 
                                <p class="no-spacing"><strong>Does Medigap Cover Prescription Drug Costs?</strong></p> 
                                <p>No, Medigap plans do not cover your out-of-pocket costs for prescription medications. This is because Medicare Supplement plans only help pay your costs for services that are covered by Original Medicare, which does not include self-administered medications.</p> 
                                <p>To get help paying for these medications, you must purchase a Part D prescription drug plan from a private insurance company. Compare your Medicare Part D plan options <a href="javascript:void(0);">here</a>.</p>
